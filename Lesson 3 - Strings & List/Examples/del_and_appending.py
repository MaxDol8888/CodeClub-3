# Create a new list
L = ['Hello','World','This','Is','A','New','List','We','Can','Modify']

# Output the contents
print(L)

# Delete the item at index 5
del L[5]

# Print the new list
print(L)

# Lets define a second list
T = ['We','Can','Also','Append','Lists','To','One','Another']

# Now we can join two lists to eachother and assign it as a new list
S = L + T

# Print out the new list.
print(S)
